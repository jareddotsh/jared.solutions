				setTimeout(function () {
					message.fadeOut(5000);
				}, 5000);
			}
			
			form.submit(function (e) {
				e.preventDefault();
				form_data = $(this).serialize();
				$.ajax({
					type: 'POST',
					url: form.attr('action'),
					data: form_data
				})
				.done(done_func)
				.fail(fail_func);
